// logoColors assigns a lipgloss color to each logo row (top ‚Üí bottom).
// The Q tail row (6) uses bright goldenrod so it visually pops.
var logoColors = []lipgloss.Color{
	colorFoliage,              // row 0 ‚ÄĒ forest green
	lipgloss.Color("#2E8B57"), // row 1 ‚ÄĒ sea green
	lipgloss.Color("#3CB371"), // row 2 ‚ÄĒ medium sea green
	colorBark,                 // row 3 ‚ÄĒ bark brown
	lipgloss.Color("#A0522D"), // row 4 ‚ÄĒ sienna
	lipgloss.Color("#CD853F"), // row 5 ‚ÄĒ peru
	colorSunlight,             // row 6 ‚ÄĒ Q tail: bright goldenrod (unmissable)
	colorMuted,                // row 7 ‚ÄĒ tagline
}

// Logo returns the SEQUOIA logo with a top-to-bottom gradient.
// Q's tail on row 6 is the key visual differentiator from O.
func Logo() string {
	var b strings.Builder
	for i, line := range logoLines {
		color := logoColors[i]
		style := lipgloss.NewStyle().Foreground(color)
		b.WriteString(style.Render(line))
		if i < len(logoLines)-1 {
			b.WriteByte('\n')
		}
	}
	return b.String()
}
